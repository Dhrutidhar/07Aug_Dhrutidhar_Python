# Q 4.) Write a Python function to get the largest number, smallest num and sum of all from a list.


list1 = [12, 3, 45, 6, 78, 9]

large = max(list1)
small = min(list1)
total = sum(list1)    
print("Largest number in list is: ",large)
print("Smallest number in list is: ",small)
print("Total of the list is: ",total)



