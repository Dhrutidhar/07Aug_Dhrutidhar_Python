# Q 8.) Write a Python program to check a list is empty or not. 


# ------------ By len() Method -------------- #

my_list = []
if len(my_list) == 0:
        print("The list is Empty!!!!")
else:
        print("List is not Empty!!!")

# ------------ By Boolean Condition --------- #

new_list = [20]
if not new_list:
        print("This list is Empty!!!")
else:
        print("List is not Emoty!!!!")

# ------------ Using conditional statement -- #

list_1 = []
if list_1 == []:
        print("This list is Empty!!!")
else:
        print("List is not Empty!!!")
